          <h3>Sejarah POPTI</h3>
          <p>
            Pada tahun 1983, beberapa orang tua anak penderita Thalasemia kerap bertemu di rumah sakit ketika anak-anak mereka menjalani transfusi darah. Saat itu, Prof. Dr. dr. Iskandar Wahidiyat, SpA(K), mendorong mereka untuk membentuk asosiasi seperti di luar negeri.
          </p>
          <p>
            Berangkat dari dorongan tersebut, pada 27 Mei 1984 terbentuklah Perhimpunan Orang Tua Penderita Thalasemia Indonesia (POPTI). Para pendiri POPTI di antaranya: Bp. R. Hikmat, MSc (alm), Bp. Ruswandi, drg. M. Ariin, Bp. S. Dutedjo, dan Ibu-ibu lainnya yang turut berjuang.
          </p>

          <h4>Tujuan POPTI:</h4>
          <ul>
            <li><i class="bi bi-check-circle"></i> Meringankan beban orang tua penderita Thalasemia</li>
            <li><i class="bi bi-check-circle"></i> Menjadi media komunikasi antar orang tua penderita</li>
            <li><i class="bi bi-check-circle"></i> Mengurangi angka penderita Thalasemia baru</li>
          </ul>

          <p>Beberapa kegiatan utama yang dijalankan oleh POPTI pusat:</p>
          <ul>
            <li><i class="bi bi-check-circle"></i> Penyediaan darah, alat medis, dan obat secara tepat waktu dan berkelanjutan</li>
            <li><i class="bi bi-check-circle"></i> Mendirikan Thalasemia Center di beberapa kota</li>
            <li><i class="bi bi-check-circle"></i> Melakukan kampanye deteksi dini Thalasemia</li>
            <li><i class="bi bi-check-circle"></i> Mengadakan pertemuan anggota, donor darah, audiensi, dan malam dana</li>
          </ul>

          <h3>Sejarah POPTI Cabang Depok</h3>
          <p>
            POPTI Cabang Depok berdiri karena tingginya jumlah penderita Thalasemia di kota ini, namun belum adanya layanan pengobatan di rumah sakit lokal.
            Ketika itu, pasien harus berobat ke RSCM, Harapan Bunda, dan PMI Bogor.
          </p>
          <p>
            Berangkat dari kondisi tersebut, Nur Ubaidilah bertekad memperjuangkan layanan Thalasemia di Kota Depok dan sejak tahun 2014 dipercaya sebagai Ketua POPTI Depok.
          </p>

          <p>
            POPTI Cabang Depok resmi berdiri pada 15 Desember 2014, dan dikukuhkan pada 22 Januari 2015 di RS Sentra Medika Cisalak oleh KH. Idris Abdul Shomad (saat itu Wakil Walikota Depok).
          </p>

          <h4>Kegiatan POPTI Depok:</h4>
          <ul>
            <li><i class="bi bi-check-circle"></i> Menyediakan kebutuhan darah bagi penderita</li>
            <li><i class="bi bi-check-circle"></i> Menjalin kerja sama dengan rumah sakit di Depok</li>
            <li><i class="bi bi-check-circle"></i> Menjadi wadah komunikasi bagi pasien dan keluarga</li>
          </ul>

          <p>
            Semoga dengan dukungan masyarakat luas, POPTI dapat terus menjadi pilar dalam menciptakan generasi Indonesia yang sehat dan bebas Thalasemia.
          </p>

